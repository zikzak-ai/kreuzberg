fn render_object_property_assertion(
    buffer: &mut String,
    var_name: &str,
    prop: &crate::fixtures::ObjectPropertyAssertion,
) -> Result<()> {
    render_object_property_assertion_with_indent(buffer, var_name, prop, "")
}

fn render_object_property_assertion_with_indent(
    buffer: &mut String,
    var_name: &str,
    prop: &crate::fixtures::ObjectPropertyAssertion,
    indent: &str,
) -> Result<()> {
    let path_parts: Vec<&str> = prop.path.split('.').collect();
    let ts_path = path_parts
        .iter()
        .map(|p| to_camel_case(p))
        .collect::<Vec<_>>()
        .join("?.");

    if let Some(exists) = prop.exists {
        if exists {
            writeln!(buffer, "        {indent}expect({var_name}.{ts_path}).toBeDefined();")?;
        } else {
            writeln!(buffer, "        {indent}expect({var_name}.{ts_path}).toBeUndefined();")?;
        }
    }

    if let Some(value) = &prop.value {
        match value {
            Value::Number(n) => {
                writeln!(buffer, "        {indent}expect({var_name}.{ts_path}).toBe({n});")?;
            }
            Value::Bool(b) => {
                writeln!(buffer, "        {indent}expect({var_name}.{ts_path}).toBe({b});")?;
            }
            Value::String(s) => {
                writeln!(
                    buffer,
                    "        {indent}expect({var_name}.{ts_path}).toBe(\"{}\");",
                    escape_ts_string(s)
                )?;
            }
            _ => {
                writeln!(
                    buffer,
                    "        {indent}expect({var_name}.{ts_path}).toEqual({});",
                    render_json_literal(value)
                )?;
            }
        }
    }

    Ok(())
}

fn to_camel_case(s: &str) -> String {
    let parts: Vec<&str> = s.split('_').collect();
    if parts.is_empty() {
        return String::new();
    }

    let mut result = parts[0].to_string();
    for part in &parts[1..] {
        if !part.is_empty() {
            let mut chars = part.chars();
            if let Some(first) = chars.next() {
                result.push_str(&first.to_uppercase().to_string());
                result.push_str(chars.as_str());
            }
        }
    }
    result
}

/// Maps a Rust target triple to a TypeScript `process.arch`/`process.platform` condition.
fn rust_triple_to_ts_condition(triple: &str) -> Option<String> {
    let (arch, platform) = match triple {
        "aarch64-unknown-linux-gnu" | "aarch64-unknown-linux-musl" => ("arm64", "linux"),
        "x86_64-unknown-linux-gnu" | "x86_64-unknown-linux-musl" => ("x64", "linux"),
        "aarch64-apple-darwin" => ("arm64", "darwin"),
        "x86_64-apple-darwin" => ("x64", "darwin"),
        "x86_64-pc-windows-msvc" => ("x64", "win32"),
        _ => return None,
    };
    Some(format!(
        "(process.arch === \"{arch}\" && process.platform === \"{platform}\")"
    ))
}

fn to_title_case(s: &str) -> String {
    s.split('_')
        .map(|word| {
            let mut chars = word.chars();
            match chars.next() {
                Some(first) => first.to_uppercase().chain(chars).collect(),
                None => String::new(),
            }
        })
        .collect::<Vec<_>>()
        .join(" ")
}
