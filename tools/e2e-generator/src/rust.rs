fn write_lib_rs(src_dir: &Utf8Path) -> Result<()> {
    let lib_rs_path = src_dir.join("lib.rs");
    let content = include_str!("../templates/rust_lib.rs");
    fs::write(&lib_rs_path, content).context("Failed to write lib.rs")?;
    Ok(())
}

fn clean_rs_files(dir: &Utf8Path) -> Result<()> {
    if !dir.exists() {
        return Ok(());
    }

    for entry in fs::read_dir(dir.as_std_path())? {
        let entry = entry?;
        let path = entry.path();
        if path.extension().is_some_and(|ext| ext == "rs") {
            fs::remove_file(path)?;
        }
    }

    Ok(())
}

fn render_category(category: &str, fixtures: &[&Fixture]) -> Result<String> {
    let mut buffer = Vec::new();
    writeln!(
        buffer,
        "// Code generated by kreuzberg-e2e-generator. DO NOT EDIT.\n// To regenerate: cargo run -p kreuzberg-e2e-generator -- generate --lang rust\n\n// Tests for {category} fixtures.\n#![allow(clippy::too_many_lines)]"
    )?;
    writeln!(buffer, "use e2e_rust::{{assertions, resolve_document}};")?;
    writeln!(buffer, "use kreuzberg::core::config::ExtractionConfig;")?;

    let needs_error_import = fixtures.iter().any(|fixture| {
        !fixture.skip().requires_feature.is_empty() || !fixture.document().requires_external_tool.is_empty()
    });

    let needs_tokio = fixtures.iter().any(|fixture| {
        let method = fixture.extraction().method;
        matches!(method, ExtractionMethod::Async | ExtractionMethod::BatchAsync)
    });

    if needs_error_import {
        writeln!(buffer, "use kreuzberg::KreuzbergError;")?;
    }

    if needs_tokio || !needs_error_import {
        writeln!(buffer)?;
    }

    for fixture in fixtures {
        buffer.write_all(render_test(fixture)?.as_bytes())?;
    }

    Ok(String::from_utf8(buffer)?)
}

fn render_test(fixture: &Fixture) -> Result<String> {
    let mut test_body = String::new();

    let extraction = fixture.extraction();
    let method = extraction.method;
    let input_type = extraction.input_type;
    let is_async = matches!(method, ExtractionMethod::Async | ExtractionMethod::BatchAsync);
    let is_batch = matches!(method, ExtractionMethod::BatchSync | ExtractionMethod::BatchAsync);

    let test_name = format!("test_{}", sanitize_identifier(&fixture.id));

    // Write test attribute
    if is_async {
        writeln!(test_body, "#[tokio::test]")?;
        writeln!(test_body, "async fn {test_name}() {{\n    // {}\n", fixture.description)?;
    } else {
        writeln!(
            test_body,
            "#[test]\nfn {test_name}() {{\n    // {}\n",
            fixture.description
        )?;
    }

    let doc_path = &fixture.document().path;
    writeln!(
        test_body,
        "    let document_path = resolve_document(\"{}\");",
        escape_rust_string(doc_path)
    )?;

    if fixture.skip().if_document_missing {
        writeln!(
            test_body,
            "    if !document_path.exists() {{\n        println!(\"Skipping {id}: missing document at {{}}\", document_path.display());\n        return;\n    }}",
            id = fixture.id
        )?;
    }

    // Emit platform skip conditions
    for platform in &fixture.skip().skip_on_platform {
        let (arch, os) = match platform.as_str() {
            "aarch64-unknown-linux-gnu" | "aarch64-unknown-linux-musl" => ("aarch64", "linux"),
            "x86_64-unknown-linux-gnu" | "x86_64-unknown-linux-musl" => ("x86_64", "linux"),
            "aarch64-apple-darwin" => ("aarch64", "macos"),
            "x86_64-apple-darwin" => ("x86_64", "macos"),
            "x86_64-pc-windows-msvc" => ("x86_64", "windows"),
            _ => continue,
        };
        writeln!(
            test_body,
            "    if cfg!(target_arch = \"{arch}\") && cfg!(target_os = \"{os}\") {{\n        println!(\"Skipping {id}: not supported on {platform}\");\n        return;\n    }}",
            id = fixture.id
        )?;
    }

    // Emit env-var skip for features that require external API keys
    if fixture.skip().requires_feature.contains(&"liter-llm".to_string()) {
        writeln!(
            test_body,
            "    if std::env::var(\"OPENAI_API_KEY\").is_err() {{\n        println!(\"Skipping {id}: OPENAI_API_KEY not set (required for liter-llm)\");\n        return;\n    }}",
            id = fixture.id
        )?;
    }

    let config_literal = render_config_literal(&extraction.config)?;
    if config_literal.trim().is_empty() || config_literal.trim() == "{}" {
        writeln!(test_body, "    let config = ExtractionConfig::default();\n")?;
    } else {
        writeln!(
            test_body,
            "    let config: ExtractionConfig = serde_json::from_str(r#\"{config}\"#)\n        .expect(\"Fixture config should deserialize\");\n",
            config = config_literal
        )?;
    }

    // For bytes input, read file contents and detect MIME type
    if input_type == InputType::Bytes {
        writeln!(
            test_body,
            "    let file_bytes = std::fs::read(&document_path)\n        .expect(\"Failed to read document file\");"
        )?;
        writeln!(
            test_body,
            "    let mime_type = kreuzberg::detect_mime_type(&document_path, true)\n        .expect(\"Failed to detect MIME type\");\n"
        )?;
    }

    // Generate extraction call based on method and input type
    // Note: extract_bytes/extract_bytes_sync require &str for mime_type, not Option
    // Note: batch functions are batch_extract_file, batch_extract_bytes with different signatures
    let extract_call = match (method, input_type, is_batch) {
        (ExtractionMethod::Sync, InputType::File, false) => {
            "kreuzberg::extract_file_sync(&document_path, None, &config)".to_string()
        }
        (ExtractionMethod::Sync, InputType::Bytes, false) => {
            "kreuzberg::extract_bytes_sync(&file_bytes, &mime_type, &config)".to_string()
        }
        (ExtractionMethod::Async, InputType::File, false) => {
            "kreuzberg::extract_file(&document_path, None, &config).await".to_string()
        }
        (ExtractionMethod::Async, InputType::Bytes, false) => {
            "kreuzberg::extract_bytes(&file_bytes, &mime_type, &config).await".to_string()
        }
        (ExtractionMethod::BatchSync, InputType::File, true) => {
            "kreuzberg::batch_extract_file_sync(vec![(document_path.clone(), None)], &config)".to_string()
        }
        (ExtractionMethod::BatchSync, InputType::Bytes, true) => {
            "kreuzberg::batch_extract_bytes_sync(vec![(file_bytes.clone(), mime_type.clone(), None)], &config)"
                .to_string()
        }
        (ExtractionMethod::BatchAsync, InputType::File, true) => {
            "kreuzberg::batch_extract_file(vec![(document_path.clone(), None)], &config).await".to_string()
        }
        (ExtractionMethod::BatchAsync, InputType::Bytes, true) => {
            "kreuzberg::batch_extract_bytes(vec![(file_bytes.clone(), mime_type.clone(), None)], &config).await"
                .to_string()
        }
        _ => "kreuzberg::extract_file_sync(&document_path, None, &config)".to_string(),
    };

    writeln!(test_body, "    let result = match {} {{", extract_call)?;

    if !fixture.skip().requires_feature.is_empty() || !fixture.document().requires_external_tool.is_empty() {
        writeln!(
            test_body,
            "        Err(KreuzbergError::MissingDependency(dep)) => {{\n            println!(\"Skipping {id}: missing dependency {{dep}}\", dep=dep);\n            return;\n        }},",
            id = fixture.id
        )?;
        writeln!(
            test_body,
            "        Err(KreuzbergError::UnsupportedFormat(fmt)) => {{\n            println!(\"Skipping {id}: unsupported format {{fmt}} (requires optional tool)\", fmt=fmt);\n            return;\n        }},",
            id = fixture.id
        )?;
        writeln!(
            test_body,
            "        Err(KreuzbergError::Parsing {{ message: ref msg, .. }}) => {{\n            println!(\"Skipping {id}: parsing dependency unavailable: {{msg}}\");\n            return;\n        }},",
            id = fixture.id
        )?;
    }

    if is_batch {
        writeln!(
            test_body,
            "        Err(err) => panic!(\"Extraction failed for {id}: {{err:?}}\"),\n        Ok(results) => results.into_iter().next().expect(\"Expected at least one result\"),\n    }};\n",
            id = fixture.id
        )?;
    } else {
        writeln!(
            test_body,
            "        Err(err) => panic!(\"Extraction failed for {id}: {{err:?}}\"),\n        Ok(result) => result,\n    }};\n",
            id = fixture.id
        )?;
    }

    test_body.push_str(&render_assertions(&fixture.assertions()));

    writeln!(test_body, "}}\n")?;

    Ok(test_body)
}

fn render_config_literal(config: &Map<String, Value>) -> Result<String> {
    if config.is_empty() {
        Ok(String::new())
    } else {
        let value = Value::Object(config.clone());
        Ok(serde_json::to_string_pretty(&value)?)
    }
}

fn render_assertions(assertions: &Assertions) -> String {
    let mut buffer = String::new();

    if !assertions.expected_mime.is_empty() {
        buffer.push_str(&format!(
            "    assertions::assert_expected_mime(&result, &{});\n",
            render_string_slice(&assertions.expected_mime)
        ));
    }

    if let Some(min) = assertions.min_content_length {
        buffer.push_str(&format!("    assertions::assert_min_content_length(&result, {min});\n"));
    }

    if let Some(max) = assertions.max_content_length {
        buffer.push_str(&format!("    assertions::assert_max_content_length(&result, {max});\n"));
    }

    if !assertions.content_contains_any.is_empty() {
        buffer.push_str(&format!(
            "    assertions::assert_content_contains_any(&result, &{});\n",
            render_string_slice(&assertions.content_contains_any)
        ));
    }

    if !assertions.content_contains_all.is_empty() {
        buffer.push_str(&format!(
            "    assertions::assert_content_contains_all(&result, &{});\n",
            render_string_slice(&assertions.content_contains_all)
        ));
    }

    if !assertions.content_contains_none.is_empty() {
        buffer.push_str(&format!(
            "    assertions::assert_content_contains_none(&result, &{});\n",
            render_string_slice(&assertions.content_contains_none)
        ));
    }

    if let Some(tables) = assertions.tables.as_ref() {
        let min = tables
            .min
            .map(|value| format!("Some({value})"))
            .unwrap_or_else(|| "None".into());
        let max = tables
            .max
            .map(|value| format!("Some({value})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!("    assertions::assert_table_count(&result, {min}, {max});\n",));
    }

    if let Some(languages) = assertions.detected_languages.as_ref() {
        let expected = render_string_slice(&languages.expects);
        let min_conf = languages
            .min_confidence
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_detected_languages(&result, &{expected}, {min_conf});\n"
        ));
    }

    if !assertions.metadata.is_empty() {
        for (path, expectation) in &assertions.metadata {
            buffer.push_str(&format!(
                "    assertions::assert_metadata_expectation(&result, \"{}\", &{});\n",
                escape_rust_string(path),
                render_json_expression(expectation)
            ));
        }
    }

    if let Some(chunks) = assertions.chunks.as_ref() {
        let min_count = chunks
            .min_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let max_count = chunks
            .max_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let each_has_content = chunks
            .each_has_content
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let each_has_embedding = chunks
            .each_has_embedding
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let each_has_heading_context = chunks
            .each_has_heading_context
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let each_has_chunk_type = chunks
            .each_has_chunk_type
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let content_starts_with_heading = chunks
            .content_starts_with_heading
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_chunks(&result, &assertions::ChunkAssertions {{\n        min_count: {min_count},\n        max_count: {max_count},\n        each_has_content: {each_has_content},\n        each_has_embedding: {each_has_embedding},\n        each_has_heading_context: {each_has_heading_context},\n        each_has_chunk_type: {each_has_chunk_type},\n        content_starts_with_heading: {content_starts_with_heading},\n    }});\n"
        ));
    }

    if let Some(images) = assertions.images.as_ref() {
        let min_count = images
            .min_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let max_count = images
            .max_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let formats_include = images
            .formats_include
            .as_ref()
            .map(|v| format!("Some(&{})", render_string_slice(v)))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_images(&result, {min_count}, {max_count}, {formats_include});\n"
        ));
    }

    if let Some(pages) = assertions.pages.as_ref() {
        let min_count = pages
            .min_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let exact_count = pages
            .exact_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let has_layout = pages
            .has_layout_regions
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let layout_classes = pages
            .layout_classes_include
            .as_ref()
            .map(|v| format!("Some(&{})", render_string_slice(v)))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_pages(&result, {min_count}, {exact_count}, {has_layout}, {layout_classes});\n"
        ));
    }

    if let Some(elements) = assertions.elements.as_ref() {
        let min_count = elements
            .min_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let types_include = elements
            .types_include
            .as_ref()
            .map(|v| format!("Some(&{})", render_string_slice(v)))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_elements(&result, {min_count}, {types_include});\n"
        ));
    }

    if let Some(ocr) = assertions.ocr_elements.as_ref() {
        let has_elements = ocr
            .has_elements
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let has_geometry = ocr
            .elements_have_geometry
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let has_confidence = ocr
            .elements_have_confidence
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let min_count = ocr
            .min_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_ocr_elements(&result, {has_elements}, {has_geometry}, {has_confidence}, {min_count});\n"
        ));
    }

    if let Some(document) = assertions.document.as_ref() {
        let has_document = document.has_document;
        let min_node_count = document
            .min_node_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let node_types = if !document.node_types_include.is_empty() {
            format!("Some(&{})", render_string_slice(&document.node_types_include))
        } else {
            "None".to_string()
        };
        let has_groups = document
            .has_groups
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_document(&result, {has_document}, {min_node_count}, {node_types}, {has_groups});\n"
        ));
    }

    if let Some(keywords) = assertions.keywords.as_ref() {
        let has_keywords = keywords
            .has_keywords
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let min_count = keywords
            .min_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let max_count = keywords
            .max_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_keywords(&result, {has_keywords}, {min_count}, {max_count});\n"
        ));
    }

    if assertions.content_not_empty == Some(true) {
        buffer.push_str("    assertions::assert_content_not_empty(&result);\n");
    }

    if let Some(tables) = assertions.tables.as_ref() {
        if let Some(has_bb) = tables.has_bounding_boxes {
            buffer.push_str(&format!(
                "    assertions::assert_table_bounding_boxes(&result, {has_bb});\n"
            ));
        }
        if let Some(ref contains) = tables.content_contains_any {
            buffer.push_str(&format!(
                "    assertions::assert_table_content_contains_any(&result, &{});\n",
                render_string_slice(contains)
            ));
        }
    }

    if let Some(images) = assertions.images.as_ref()
        && let Some(has_bb) = images.has_bounding_boxes
    {
        buffer.push_str(&format!(
            "    assertions::assert_image_bounding_boxes(&result, {has_bb});\n"
        ));
    }

    if let Some(quality) = assertions.quality_score.as_ref() {
        let has_score = quality
            .has_score
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let min_score = quality
            .min_score
            .map(|v| format!("Some({v:?})"))
            .unwrap_or_else(|| "None".into());
        let max_score = quality
            .max_score
            .map(|v| format!("Some({v:?})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_quality_score(&result, {has_score}, {min_score}, {max_score});\n"
        ));
    }

    if let Some(warnings) = assertions.processing_warnings.as_ref() {
        let max_count = warnings
            .max_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let is_empty = warnings
            .is_empty
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_processing_warnings(&result, {max_count}, {is_empty});\n"
        ));
    }

    if let Some(llm_usage) = assertions.llm_usage.as_ref() {
        let max_count = llm_usage
            .max_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let is_empty = llm_usage
            .is_empty
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_llm_usage(&result, {max_count}, {is_empty});\n"
        ));
    }

    if let Some(djot) = assertions.djot_content.as_ref() {
        let has_content = djot
            .has_content
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let min_blocks = djot
            .min_blocks
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_djot_content(&result, {has_content}, {min_blocks});\n"
        ));
    }

    if let Some(annotations) = assertions.annotations.as_ref() {
        let has_annotations = annotations.has_annotations;
        let min_count = annotations
            .min_count
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        buffer.push_str(&format!(
            "    assertions::assert_annotations(&result, {has_annotations}, {min_count});\n"
        ));
    }

    if let Some(structured) = assertions.structured_output.as_ref() {
        let has_output = structured
            .has_output
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let validates_schema = structured
            .validates_schema
            .map(|v| format!("Some({v})"))
            .unwrap_or_else(|| "None".into());
        let field_exists = if let Some(ref fields) = structured.field_exists {
            let parts = fields
                .iter()
                .map(|f| format!("\"{}\"", f))
                .collect::<Vec<_>>()
                .join(", ");
            format!("Some(&[{}])", parts)
        } else {
            "None".into()
        };
        buffer.push_str(&format!(
            "    assertions::assert_structured_output(&result, {has_output}, {validates_schema}, {field_exists});\n"
        ));
    }

    buffer
}

fn render_json_expression(value: &serde_json::Value) -> String {
    format!("serde_json::json!({})", value)
}

fn render_string_slice(values: &[String]) -> String {
    let parts = values
        .iter()
        .map(|value| format!("\"{}\"", escape_rust_string(value)))
        .collect::<Vec<_>>()
        .join(", ");
    format!("[{}]", parts)
}

fn sanitize_identifier(input: &str) -> String {
    let mut ident = input
        .chars()
        .map(|c| match c {
            'a'..='z' | 'A'..='Z' | '0'..='9' => c.to_ascii_lowercase(),
            _ => '_',
        })
        .collect::<String>();
    while ident.contains("__") {
        ident = ident.replace("__", "_");
    }
    ident.trim_matches('_').to_string()
}

fn escape_rust_string(value: &str) -> String {
    value
        .replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\t', "\\t")
}

