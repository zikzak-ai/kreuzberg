pub fn generate(fixtures: &[Fixture], output_root: &Utf8Path, mode: &GenerationMode) -> Result<()> {
    let rust_root = output_root.join("rust");
    let tests_dir = rust_root.join("tests");
    let fixtures_dir = tests_dir.join("fixtures");
    let src_dir = rust_root.join("src");

    fs::create_dir_all(&fixtures_dir).context("Failed to create Rust tests fixtures directory")?;
    fs::create_dir_all(&src_dir).context("Failed to create Rust src directory")?;

    write_cargo_toml(&rust_root, mode)?;
    write_lib_rs(&src_dir)?;
    clean_rs_files(&tests_dir)?;
    clean_rs_files(&fixtures_dir)?;

    let doc_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_document_extraction()).collect();
    let api_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_plugin_api()).collect();
    let mut render_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_render()).collect();

    let mut generated_modules = Vec::new();

    let mut grouped = doc_fixtures
        .into_iter()
        .into_group_map_by(|fixture| fixture.category().to_string())
        .into_iter()
        .collect::<Vec<_>>();
    grouped.sort_by(|a, b| a.0.cmp(&b.0));

    for (category, mut fixtures) in grouped {
        fixtures.sort_by(|a, b| a.id.cmp(&b.id));
        let module_name = format!("{}_test", sanitize_identifier(&category));
        let file_name = format!("{}.rs", module_name);
        let content = render_category(&category, &fixtures)?;
        let path = fixtures_dir.join(file_name);
        fs::write(&path, content).with_context(|| format!("Writing {}", path))?;
        generated_modules.push(module_name);
    }

    if !api_fixtures.is_empty() {
        generate_plugin_api_tests(&api_fixtures, &fixtures_dir)?;
        generated_modules.push("plugin_apis_test".to_string());
    }

    if !render_fixtures.is_empty() {
        render_fixtures.sort_by(|a, b| a.id.cmp(&b.id));
        let content = render_render_tests(&render_fixtures)?;
        let path = fixtures_dir.join("render_test.rs");
        fs::write(&path, content).with_context(|| format!("Writing {}", path))?;
        generated_modules.push("render_test".to_string());
    }

    let mut embed_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_embed()).collect();
    if !embed_fixtures.is_empty() {
        embed_fixtures.sort_by(|a, b| a.id.cmp(&b.id));
        let content = generate_embed_tests_rust(&embed_fixtures)?;
        let path = fixtures_dir.join("embed_test.rs");
        fs::write(&path, content).with_context(|| format!("Writing {}", path))?;
        generated_modules.push("embed_test".to_string());
    }

    // Generate fixtures/mod.rs
    let mut mod_content = "// Code generated by kreuzberg-e2e-generator. DO NOT EDIT.\n\n".to_string();
    generated_modules.sort();
    for module in generated_modules {
        writeln!(mod_content, "pub mod {};", module)?;
    }
    fs::write(fixtures_dir.join("mod.rs"), mod_content).context("Failed to write fixtures/mod.rs")?;

    // Generate tests/e2e.rs as the single entry point
    let e2e_content = "// Code generated by kreuzberg-e2e-generator. DO NOT EDIT.\n\nmod fixtures;\n";
    fs::write(tests_dir.join("e2e.rs"), e2e_content).context("Failed to write tests/e2e.rs")?;

    let mut embed_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_embed()).collect();
    if !embed_fixtures.is_empty() {
        embed_fixtures.sort_by(|a, b| a.id.cmp(&b.id));
        let content = generate_embed_tests_rust(&embed_fixtures)?;
        let path = tests_dir.join("embed_test.rs");
        fs::write(&path, content).with_context(|| format!("Writing {}", path))?;
    }

    Ok(())
}

