/// Map serde-renamed JSON field names to actual Rust struct field names.
fn serde_to_rust_field(field: &str) -> &str {
    match field {
        "max_chars" => "max_characters",
        "max_overlap" => "overlap",
        _ => field,
    }
}

fn generate_object_property_assertions_rust(
    assertions: &crate::fixtures::PluginAssertions,
    buf: &mut String,
) -> Result<()> {
    if !assertions.object_properties.is_empty() {
        for prop in &assertions.object_properties {
            let path = &prop.path;

            if let Some(exists) = prop.exists {
                if exists {
                    writeln!(buf, "    // Verify {} exists", path)?;

                    if path.contains('.') {
                        let parts: Vec<&str> = path.split('.').collect();
                        if parts.len() == 2 {
                            writeln!(buf, "    assert!(config.{}.is_some());", parts[0])?;
                        }
                    } else {
                        writeln!(buf, "    let _ = &config.{};", path)?;
                    }
                } else {
                    writeln!(buf, "    // Verify {} does not exist (not implemented)", path)?;
                }
            }

            if let Some(value) = &prop.value {
                if path.contains('.') {
                    let parts: Vec<&str> = path.split('.').collect();
                    if parts.len() == 2 {
                        let rust_field = serde_to_rust_field(parts[1]);
                        match value {
                            Value::Number(n) => {
                                writeln!(
                                    buf,
                                    "    assert_eq!(config.{}.as_ref().unwrap().{}, {});",
                                    parts[0], rust_field, n
                                )?;
                            }
                            Value::String(s) => {
                                writeln!(
                                    buf,
                                    "    assert_eq!(config.{}.as_ref().unwrap().{}, \"{}\");",
                                    parts[0],
                                    rust_field,
                                    escape_rust_string(s)
                                )?;
                            }
                            Value::Bool(b) => {
                                if *b {
                                    writeln!(
                                        buf,
                                        "    assert!(config.{}.as_ref().unwrap().{});",
                                        parts[0], rust_field
                                    )?;
                                } else {
                                    writeln!(
                                        buf,
                                        "    assert!(!config.{}.as_ref().unwrap().{});",
                                        parts[0], rust_field
                                    )?;
                                }
                            }
                            _ => {
                                writeln!(buf, "    // Complex value assertion not yet implemented for {}", path)?;
                            }
                        }
                    }
                } else {
                    match value {
                        Value::Number(n) => {
                            writeln!(buf, "    assert_eq!(config.{}, {});", path, n)?;
                        }
                        Value::String(s) => {
                            writeln!(buf, "    assert_eq!(config.{}, \"{}\");", path, escape_rust_string(s))?;
                        }
                        Value::Bool(b) => {
                            if *b {
                                writeln!(buf, "    assert!(config.{});", path)?;
                            } else {
                                writeln!(buf, "    assert!(!config.{});", path)?;
                            }
                        }
                        _ => {
                            writeln!(buf, "    // Complex value assertion not yet implemented for {}", path)?;
                        }
                    }
                }
            }
        }
    }

    Ok(())
}

fn generate_embed_tests_rust(fixtures: &[&Fixture]) -> Result<String> {
    let mut buf = String::new();
    writeln!(buf, "// Code generated by kreuzberg-e2e-generator. DO NOT EDIT.")?;
    writeln!(
        buf,
        "// To regenerate: cargo run -p kreuzberg-e2e-generator -- generate --lang rust"
    )?;
    writeln!(buf, "\n// Tests for standalone embed() fixtures.")?;
    writeln!(buf, "#![allow(clippy::too_many_lines, unreachable_code)]")?;
    writeln!(buf, "use e2e_rust::assertions;")?;
    writeln!(
        buf,
        "use kreuzberg::core::config::{{EmbeddingConfig, EmbeddingModelType}};"
    )?;
    writeln!(buf)?;

    for fixture in fixtures {
        buf.push_str(&render_embed_test(fixture)?);
    }

    Ok(buf)
}

fn render_embed_test(fixture: &Fixture) -> Result<String> {
    let mut code = String::new();
    let spec = fixture.embed_spec();
    let assertions = fixture.embed_assertions();
    let is_async = spec.async_variant;
    let test_name = format!("test_{}", sanitize_identifier(&fixture.id));

    if is_async {
        writeln!(code, "#[tokio::test]")?;
        writeln!(code, "async fn {test_name}() {{")?;
    } else {
        writeln!(code, "#[test]")?;
        writeln!(code, "fn {test_name}() {{")?;
    }
    writeln!(code, "    // {}", escape_rust_string(&fixture.description))?;

    if !fixture.skip().requires_feature.is_empty() {
        writeln!(code, "    #[cfg(not(feature = \"embeddings\"))]\n    return;")?;
    }

    let skip_platforms = &fixture.skip().skip_on_platform;
    if !skip_platforms.is_empty() {
        let conditions = skip_platforms
            .iter()
            .filter_map(|p| match p.as_str() {
                "x86_64-pc-windows-msvc" => Some("all(target_os = \"windows\", target_env = \"msvc\")"),
                "aarch64-apple-darwin" => Some("all(target_arch = \"aarch64\", target_os = \"macos\")"),
                _ => None,
            })
            .collect::<Vec<_>>();
        if !conditions.is_empty() {
            if conditions.len() == 1 {
                writeln!(code, "    #[cfg({})]", conditions[0])?;
            } else {
                let combined = conditions.join(", ");
                writeln!(code, "    #[cfg(any({}))]", combined)?;
            }
            writeln!(code, "    return;")?;
        }
    }

    let model_name = spec
        .config
        .get("model")
        .and_then(|m| m.get("name"))
        .and_then(|n| n.as_str())
        .unwrap_or("balanced");
    let normalize = spec.config.get("normalize").and_then(|v| v.as_bool()).unwrap_or(false);
    let batch_size = spec.config.get("batch_size").and_then(|v| v.as_u64());

    writeln!(
        code,
        "    let model = EmbeddingModelType::Preset {{ name: \"{model_name}\".to_string() }};"
    )?;
    if let Some(bs) = batch_size {
        writeln!(
            code,
            "    let config = EmbeddingConfig {{\n        model,\n        normalize: {normalize},\n        batch_size: {bs},\n        ..Default::default()\n    }};"
        )?;
    } else {
        writeln!(
            code,
            "    let config = EmbeddingConfig {{\n        model,\n        normalize: {normalize},\n        ..Default::default()\n    }};"
        )?;
    }
    writeln!(code)?;

    let texts_literal = spec
        .texts
        .iter()
        .map(|s| format!("\"{}\"", escape_rust_string(s)))
        .collect::<Vec<_>>()
        .join(", ");
    if spec.texts.is_empty() {
        writeln!(code, "    let texts: Vec<&str> = vec![];")?;
    } else {
        writeln!(code, "    let texts = vec![{texts_literal}];")?;
    }

    if is_async {
        writeln!(
            code,
            "    let result_res = kreuzberg::embed_texts_async(texts, &config).await;"
        )?;
    } else {
        writeln!(code, "    let result_res = kreuzberg::embed_texts(&texts, &config);")?;
    }

    if spec
        .config
        .get("model")
        .and_then(|m| m.get("name"))
        .is_some_and(|n| n.as_str() == Some("invalid"))
    {
        writeln!(code, "    assert!(result_res.is_err());\n}}")?;
        return Ok(code);
    } else {
        writeln!(code, "    let result = result_res.expect(\"Embedding failed\");")?;
    }

    let count_lit = assertions
        .count
        .map(|c| format!("Some({c})"))
        .unwrap_or_else(|| "None".into());
    let dim_lit = assertions
        .dimensions
        .map(|d| format!("Some({d})"))
        .unwrap_or_else(|| "None".into());
    writeln!(
        code,
        "    assertions::assert_embed_result(&result, {count_lit}, {dim_lit}, {no_nan}, {no_inf}, {non_zero}, {normalized});\n}}\n",
        no_nan = assertions.no_nan,
        no_inf = assertions.no_inf,
        non_zero = assertions.non_zero,
        normalized = assertions.normalized,
    )?;

    Ok(code)
}

pub fn generate_parity(manifest: &ParityManifest, output_root: &Utf8Path, _mode: &GenerationMode) -> Result<()> {
    let rust_root = output_root.join("rust");
    let tests_dir = rust_root.join("tests");
    fs::create_dir_all(&tests_dir).context("Failed to create Rust tests directory")?;

    let lang = "rust";
    let mut buf = String::new();

    writeln!(buf, "// Code generated by kreuzberg-e2e-generator. DO NOT EDIT.")?;
    writeln!(
        buf,
        "// To regenerate: cargo run -p kreuzberg-e2e-generator -- generate --lang rust"
    )?;
    writeln!(buf)?;

    // Collect all struct types that have fields for this language
    let mut struct_types: Vec<(&String, std::collections::BTreeMap<String, parity::FieldDef>)> = Vec::new();
    for (type_name, type_def) in &manifest.types {
        if let TypeDef::Struct { .. } = type_def
            && let Some(fields) = parity::fields_for_type_and_lang(manifest, type_name, lang)
        {
            struct_types.push((type_name, fields));
        }
    }

    // Types that implement Default and can be tested via serde round-trip.
    // Other struct types are tested via compile-time field existence checks.
    let types_with_default: &[&str] = &["ExtractionResult", "ExtractionConfig"];

    // Emit a test for each struct type
    for (type_name, fields) in &struct_types {
        let fn_name = format!("test_{}_field_parity", to_snake_case(type_name));
        let required_fields: Vec<&String> = fields
            .iter()
            .filter(|(_, f)| f.required)
            .map(|(name, _)| name)
            .collect();
        let all_fields: Vec<&String> = fields.keys().collect();

        let rust_path = rust_type_path(type_name);

        if types_with_default.contains(&type_name.as_str()) {
            // Types with Default: use serde round-trip to verify fields
            writeln!(buf, "#[test]")?;
            writeln!(buf, "fn {fn_name}() {{")?;
            writeln!(
                buf,
                "    // Verify {type_name} has the expected fields via serde serialization"
            )?;
            writeln!(buf, "    let required_fields: &[&str] = &[")?;
            for field in &required_fields {
                writeln!(buf, "        \"{field}\",")?;
            }
            writeln!(buf, "    ];")?;
            writeln!(buf, "    let all_known_fields: &[&str] = &[")?;
            for field in &all_fields {
                writeln!(buf, "        \"{field}\",")?;
            }
            writeln!(buf, "    ];")?;
            writeln!(buf)?;
            writeln!(
                buf,
                "    let json = serde_json::to_value({rust_path}::default()).unwrap();"
            )?;
            writeln!(buf, "    let obj = json.as_object().unwrap();")?;
            writeln!(buf)?;
            writeln!(
                buf,
                "    // All required fields must be present in the serialized default"
            )?;
            writeln!(buf, "    for field in required_fields {{")?;
            writeln!(
                buf,
                "        assert!(obj.contains_key(*field), \"{type_name} missing required field: {{field}}\");"
            )?;
            writeln!(buf, "    }}")?;
            writeln!(buf)?;
            writeln!(
                buf,
                "    // No unexpected fields should appear in the serialized output"
            )?;
            writeln!(buf, "    for key in obj.keys() {{")?;
            writeln!(
                buf,
                "        assert!(all_known_fields.contains(&key.as_str()), \"{type_name} has unexpected field: {{key}}\");"
            )?;
            writeln!(buf, "    }}")?;
            writeln!(buf, "}}")?;
            writeln!(buf)?;
        } else {
            // Types without Default: compile-time field existence check via std::mem::offset_of!
            // We verify the type exists and has the expected fields by naming them in a pattern.
            writeln!(buf, "#[test]")?;
            writeln!(buf, "fn {fn_name}() {{")?;
            writeln!(
                buf,
                "    // Compile-time check that {type_name} exists and has the expected fields."
            )?;
            writeln!(
                buf,
                "    // If a field is added/removed in the Rust type, this will fail to compile."
            )?;
            writeln!(buf, "    let expected_fields: &[&str] = &[")?;
            for field in &all_fields {
                writeln!(buf, "        \"{field}\",")?;
            }
            writeln!(buf, "    ];")?;
            writeln!(
                buf,
                "    // Verify the type is constructible (fields exist at compile time)"
            )?;
            writeln!(buf, "    fn _assert_fields_exist(v: &{rust_path}) {{")?;
            for field in &all_fields {
                writeln!(buf, "        let _ = &v.{field};")?;
            }
            writeln!(buf, "    }}")?;
            writeln!(
                buf,
                "    assert!(!expected_fields.is_empty(), \"{type_name} parity check has fields\");"
            )?;
            writeln!(buf, "}}")?;
            writeln!(buf)?;
        }
    }

    let path = tests_dir.join("parity_test.rs");
    fs::write(&path, &buf).with_context(|| format!("Writing {path}"))?;

    Ok(())
}

fn to_snake_case(s: &str) -> String {
    let mut result = String::with_capacity(s.len() + 4);
    for (i, ch) in s.chars().enumerate() {
        if ch.is_uppercase() && i > 0 {
            result.push('_');
        }
        result.push(ch.to_ascii_lowercase());
    }
    result
}

/// Map a parity manifest type name to its full Rust path.
///
/// Most types are re-exported at `kreuzberg::TypeName`, but some live in
/// sub-modules (e.g. `kreuzberg::keywords::Keyword`).
fn rust_type_path(type_name: &str) -> String {
    match type_name {
        "Keyword" => "kreuzberg::keywords::Keyword".to_string(),
        _ => format!("kreuzberg::{type_name}"),
    }
}
