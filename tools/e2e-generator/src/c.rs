pub fn generate(fixtures: &[Fixture], output_root: &Utf8Path, mode: &GenerationMode) -> Result<()> {
    let c_root = output_root.join("c");
    fs::create_dir_all(&c_root).context("failed to create c e2e directory")?;

    write_helpers(&c_root)?;
    generate_embed_tests(fixtures, &c_root)?;
    clean_tests(&c_root)?;

    let doc_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_document_extraction()).collect();
    let plugin_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_plugin_api()).collect();

    let mut grouped = doc_fixtures
        .into_iter()
        .into_group_map_by(|fixture| fixture.category().to_string())
        .into_iter()
        .collect::<Vec<_>>();
    grouped.sort_by(|a, b| a.0.cmp(&b.0));

    let mut test_names: Vec<String> = Vec::new();

    for (category, mut fixtures) in grouped {
        fixtures.sort_by(|a, b| a.id.cmp(&b.id));
        let stem = format!("test_{}", category.to_lowercase());
        let filename = format!("{stem}.c");
        let content = render_category(&category, &fixtures)?;
        fs::write(c_root.join(&filename), content)
            .with_context(|| format!("failed to write C test file {filename}"))?;
        test_names.push(stem);
    }

    if !plugin_fixtures.is_empty() {
        generate_plugin_api_tests(&c_root, &plugin_fixtures)?;
        test_names.push("test_plugin_apis".to_string());
    }

    let render_fixtures: Vec<_> = fixtures.iter().filter(|f| f.is_render()).collect();
    if !render_fixtures.is_empty() {
        let mut sorted = render_fixtures;
        sorted.sort_by(|a, b| a.id.cmp(&b.id));
        let content = render_render_category_c(&sorted)?;
        fs::write(c_root.join("test_render.c"), content).context("Failed to write C render test file")?;
        test_names.push("test_render".to_string());
    }

    // Parity test (generated separately via generate_parity, but needs a Makefile entry)
    test_names.push("test_parity".to_string());

    write_makefile_with_tests(&c_root, &test_names, mode)?;

    write_setup_script(&c_root, mode)?;

    Ok(())
}

fn write_setup_script(c_root: &Utf8Path, mode: &GenerationMode) -> Result<()> {
    if !mode.is_published() {
        return Ok(());
    }
    let setup = c_root.join("setup.sh");
    fs::write(
        &setup,
        r#"#!/usr/bin/env bash
set -euo pipefail
echo "Setting up C test app..."
# Install kreuzberg-ffi library
cargo install kreuzberg-cli
echo "Setup complete. Ensure kreuzberg FFI headers and library are in ~/.kreuzberg/"
"#,
    )
    .context("Failed to write setup.sh")?;
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        fs::set_permissions(&setup, fs::Permissions::from_mode(0o755))?;
    }
    Ok(())
}

