/// Map a manifest field name to the corresponding CExtractionResult struct field name.
/// In the C FFI, non-string fields (object, array, number) get a `_json` suffix.
fn c_struct_field_name(manifest_field: &str, json_type: &str) -> String {
    match json_type {
        "string" | "boolean" => manifest_field.to_string(),
        _ => format!("{manifest_field}_json"),
    }
}

pub fn generate_parity(manifest: &ParityManifest, output_root: &Utf8Path, _mode: &GenerationMode) -> Result<()> {
    let c_root = output_root.join("c");
    fs::create_dir_all(&c_root).context("failed to create c e2e directory")?;

    let lang = "c";
    let mut buf = String::new();

    writeln!(buf, "/* Code generated by kreuzberg-e2e-generator. DO NOT EDIT. */")?;
    writeln!(
        buf,
        "/* Parity tests: verify CExtractionResult struct has all expected fields. */"
    )?;
    writeln!(buf)?;
    writeln!(buf, "#include \"helpers.h\"")?;
    writeln!(buf, "#include <stdio.h>")?;
    writeln!(buf, "#include <stddef.h>")?;
    writeln!(buf)?;

    // Get ExtractionResult fields filtered for the "ffi" profile
    if let Some(fields) = parity::fields_for_type_and_lang(manifest, "ExtractionResult", lang) {
        writeln!(buf, "static void test_parity_extraction_result_fields(void) {{")?;
        writeln!(buf, "    /* Extract a simple text file to get a populated result */")?;
        writeln!(
            buf,
            "    CExtractionResult *result = run_extraction(\"text/report.txt\", NULL);"
        )?;
        writeln!(buf, "    if (!result) return; /* skipped */")?;
        writeln!(buf)?;

        writeln!(buf, "    /* Verify required fields are non-NULL */")?;
        for (field_name, field_def) in &fields {
            let c_field = c_struct_field_name(field_name, &field_def.json_type);
            if field_def.required && field_def.json_type == "string" {
                writeln!(
                    buf,
                    "    if (!result->{c_field}) {{ fprintf(stderr, \"FAIL: CExtractionResult missing required field: {c_field}\\n\"); exit(1); }}"
                )?;
            }
        }
        writeln!(buf)?;

        writeln!(
            buf,
            "    /* Verify all expected fields are accessible via struct offset */"
        )?;
        writeln!(
            buf,
            "    /* (compile-time check — if a field is missing, this file will not compile) */"
        )?;
        for (field_name, field_def) in &fields {
            let c_field = c_struct_field_name(field_name, &field_def.json_type);
            writeln!(
                buf,
                "    (void)offsetof(CExtractionResult, {c_field}); /* {field_name} */"
            )?;
        }
        writeln!(buf)?;

        writeln!(buf, "    printf(\"  PASS: all ExtractionResult fields present\\n\");")?;
        writeln!(buf, "    kreuzberg_free_result(result);")?;
        writeln!(buf, "}}")?;
        writeln!(buf)?;
    }

    // main()
    writeln!(buf, "int main(void) {{")?;
    writeln!(buf, "    test_parity_extraction_result_fields();")?;
    writeln!(buf, "    printf(\"test_parity: all tests passed\\n\");")?;
    writeln!(buf, "    return 0;")?;
    writeln!(buf, "}}")?;

    fs::write(c_root.join("test_parity.c"), &buf).context("failed to write C parity test file")?;

    Ok(())
}

