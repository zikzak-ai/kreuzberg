fn render_embed_test_c(fixture: &Fixture) -> Result<(String, String)> {
    let fn_name = format!("test_embed_{}", to_snake_safe(&fixture.id));
    let mut code = String::new();
    let embed = fixture.embed.as_ref().expect("embed spec required");
    let fixture_assertions = fixture.assertions();
    let assertions = fixture_assertions.embed.as_ref().expect("embed assertions required");

    writeln!(code, "static void {fn_name}(void) {{")?;

    if !fixture.id.contains("disabled") {
        writeln!(code, "#if defined(_WIN32) && defined(_M_X64)")?;
        writeln!(
            code,
            "    printf(\"SKIP: %s (embeddings on Windows X64 until ONNX is implemented)\\n\", \"{}\");",
            fixture.id
        )?;
        writeln!(code, "    return;")?;
        writeln!(code, "#endif")?;
    }

    let texts_arr = render_string_array(&embed.texts);
    let texts_count = embed.texts.len();
    let config_lit = render_config_literal(&embed.config)?;

    let func = if embed.async_variant {
        "kreuzberg_embed_texts"
    } else {
        "kreuzberg_embed_texts_sync"
    };

    writeln!(
        code,
        "    CEmbedResult *result = {func}({texts_arr}, {texts_count}, {config_lit});"
    )?;

    let count = assertions.count.map(|c| c as i32).unwrap_or(-1);
    let dimensions = assertions.dimensions.map(|d| d as i32).unwrap_or(-1);

    writeln!(
        code,
        "    assert_embed_result(result, {}, {}, {}, {}, {}, {}, {});",
        if count >= 0 { 1 } else { 0 },
        if count >= 0 { count as u64 } else { 0 },
        if dimensions >= 0 { 1 } else { 0 },
        if dimensions >= 0 { dimensions as u64 } else { 0 },
        if assertions.no_nan { 1 } else { 0 },
        if assertions.no_inf { 1 } else { 0 },
        if assertions.non_zero { 1 } else { 0 }
    )?;

    writeln!(code, "    kreuzberg_free_embed_result(result);")?;
    writeln!(code, "}}")?;

    Ok((fn_name, code))
}
