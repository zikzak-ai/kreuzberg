fn render_render_test_csharp(buffer: &mut String, fixture: &Fixture) -> Result<()> {
    let render = fixture.render.as_ref().expect("render spec required");
    let assertions = fixture.assertions().render.unwrap_or_default();
    let test_name = sanitize_method_name(&fixture.id);
    let doc_path = escape_csharp_string(&fixture.document().path);

    writeln!(buffer, "        [SkippableFact]")?;
    writeln!(buffer, "        public void {test_name}()")?;
    writeln!(buffer, "        {{")?;
    writeln!(
        buffer,
        "            var documentPath = TestHelpers.EnsureDocument(\"{doc_path}\", true);"
    )?;

    let dpi_arg = render.dpi.map(|d| format!(", {d}")).unwrap_or_default();

    match render.mode.as_str() {
        "single_page" => {
            let page_index = render.page_index.unwrap_or(0);
            writeln!(
                buffer,
                "            var pngData = KreuzbergClient.RenderPdfPage(documentPath, {page_index}{dpi_arg});"
            )?;
            render_render_assertions_csharp(&assertions, "pngData", buffer, "            ")?;
        }
        "iterator" => {
            writeln!(buffer, "            var pageCount = 0;")?;
            writeln!(
                buffer,
                "            using (var iter = PdfPageIterator.Open(documentPath{dpi_arg}))"
            )?;
            writeln!(buffer, "            {{")?;
            writeln!(buffer, "                foreach (var pngData in iter)")?;
            writeln!(buffer, "                {{")?;
            writeln!(buffer, "                    TestHelpers.AssertIsPng(pngData.Data);")?;
            writeln!(buffer, "                    pageCount++;")?;
            writeln!(buffer, "                }}")?;
            writeln!(buffer, "            }}")?;
            if let Some(page_count_gte) = assertions.page_count_gte {
                writeln!(buffer, "            Assert.True(pageCount >= {page_count_gte},")?;
                writeln!(
                    buffer,
                    "                $\"Expected at least {page_count_gte} pages, got {{pageCount}}\");"
                )?;
            }
        }
        _ => anyhow::bail!("Unknown render mode: {}", render.mode),
    }

    writeln!(buffer, "        }}")?;
    writeln!(buffer)?;
    Ok(())
}

